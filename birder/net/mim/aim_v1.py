class MLPBlock(nn.Module):
    def __init__(self, dim: int) -> None:
        super().__init__()

        self.norm1 = nn.LayerNorm(dim)
        self.mlp = nn.Sequential(
            nn.Linear(dim, dim * 4, bias=False),
            nn.GELU(),
            nn.Linear(dim * 4, dim, bias=False),
        )
        self.norm2 = nn.LayerNorm(dim)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        x = x + self.norm1(x)
        return x + self.mlp(self.norm2(x))


class AIM_v1(MIMBaseNet):
    default_size = (224, 224)
    default_mask_ratio = 0.0  # Not used
    auto_register = False

    def __init__(
        self,
        encoder: PreTrainEncoder,
        *,
        config: Optional[dict[str, Any]] = None,
        size: Optional[tuple[int, int]] = None,
        mask_ratio: Optional[float] = None,
        min_mask_size: int = 1,
    ) -> None:
        super().__init__(encoder, config=config, size=size, mask_ratio=mask_ratio, min_mask_size=min_mask_size)
        assert self.config is not None, "must set config"

        self.patch_size = self.encoder.stem_stride
        encoder_dim = self.encoder.feature_dim
        decoder_embed_dim: int = self.config["decoder_embed_dim"]
        decoder_depth: int = self.config["decoder_depth"]

        self.decoder_embed = nn.Linear(encoder_dim, decoder_embed_dim)

        # Fixed sin-cos embedding
        pos_embedding = pos_embedding_sin_cos_2d(
            h=self.size[0] // self.patch_size,
            w=self.size[1] // self.patch_size,
            dim=decoder_embed_dim,
            num_special_tokens=0,
        )
        self.decoder_pos_embed = nn.Buffer(pos_embedding.unsqueeze(0))

        layers = []
        for _ in range(decoder_depth):
            layers.append(MLPBlock(decoder_embed_dim))

        self.decoder_blocks = nn.Sequential(*layers)
        self.decoder_norm = nn.LayerNorm(decoder_embed_dim)
        self.decoder_pred = nn.Linear(decoder_embed_dim, self.patch_size**2 * self.input_channels)

        # Weight initialization
        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight)
                if m.bias is not None:
                    nn.init.zeros_(m.bias)

            elif isinstance(m, nn.LayerNorm):
                nn.init.ones_(m.weight)
                nn.init.zeros_(m.bias)

        scale = 1.0 / (1 + decoder_depth)
        std = math.sqrt(scale / self.decoder_pred.in_features)
        nn.init.trunc_normal_(self.decoder_pred.weight, std=std)
        nn.init.zeros_(self.decoder_pred.bias)

    def patchify(self, imgs: torch.Tensor) -> torch.Tensor:
        """
        imgs: (N, C, H, W)
        x: (N, L, patch_size**2 * C)
        """

        p = self.patch_size
        c = self.input_channels
        assert imgs.shape[2] % p == 0 and imgs.shape[3] % p == 0

        h = imgs.shape[2] // p
        w = imgs.shape[3] // p
        x = imgs.reshape(shape=(imgs.shape[0], c, h, p, w, p))
        x = torch.einsum("nchpwq->nhwpqc", x)
        x = x.reshape(shape=(imgs.shape[0], h * w, p**2 * c))

        return x

    def unpatchify(self, x: torch.Tensor) -> torch.Tensor:
        """
        x: (N, L, patch_size**2 * C)
        imgs: (N, C, H, W)
        """

        p = self.patch_size
        c = self.input_channels
        h = self.size[0] // p
        w = self.size[1] // p
        assert h * w == x.shape[1]

        x = x.reshape(shape=(x.size(0), h, w, p, p, c))
        x = torch.einsum("nhwpqc->nchpwq", x)
        imgs = x.reshape(shape=(x.size(0), c, h * p, w * p))

        return imgs

    def _sample_prefix_lengths(self, batch_size: int, num_patches: int, device: torch.device) -> torch.Tensor:
        prefix_max = min(255, num_patches - 1)
        return torch.randint(1, prefix_max + 1, (batch_size,), dtype=torch.long, device=device)

    def _make_prefix_mask(self, prefix_lengths: torch.Tensor, num_patches: int) -> torch.Tensor:
        patch_idx = torch.arange(num_patches, device=prefix_lengths.device).unsqueeze(0)
        return patch_idx >= prefix_lengths.unsqueeze(1)

    def _make_attention_mask(self, prefix_mask: torch.Tensor, num_special_tokens: int) -> torch.Tensor:
        B, K = prefix_mask.size()
        device = prefix_mask.device

        prefix_keys = ~prefix_mask
        causal_mask = torch.ones(K, K, dtype=torch.bool, device=device).tril(diagonal=0)
        patch_attn_mask = causal_mask.unsqueeze(0) | prefix_keys.unsqueeze(1)

        if num_special_tokens == 0:
            return patch_attn_mask.unsqueeze(1)

        seq_len = num_special_tokens + K
        attn_mask = torch.zeros(B, seq_len, seq_len, dtype=torch.bool, device=device)
        attn_mask[:, :num_special_tokens, :num_special_tokens] = True
        attn_mask[:, :num_special_tokens, num_special_tokens:] = prefix_keys.unsqueeze(1)
        attn_mask[:, num_special_tokens:, :num_special_tokens] = True
        attn_mask[:, num_special_tokens:, num_special_tokens:] = patch_attn_mask

        return attn_mask.unsqueeze(1)

    def forward_loss(self, x: torch.Tensor, pred: torch.Tensor, mask: torch.Tensor) -> torch.Tensor:
        target = self.patchify(x)
        mean = target.mean(dim=-1, keepdim=True)
        var = target.var(dim=-1, keepdim=True)
        target = (target - mean) / (var + 1.0e-6) ** 0.5

        loss = (pred[:, :-1] - target[:, 1:]) ** 2
        loss = loss.mean(dim=-1)
        mask = mask[:, 1:].type_as(loss)
        loss = (loss * mask).sum() / mask.sum()

        return loss

    def forward(self, x: torch.Tensor) -> dict[str, torch.Tensor]:
        h = self.size[0] // self.patch_size
        w = self.size[1] // self.patch_size
        num_patches = h * w

        prefix_lengths = self._sample_prefix_lengths(x.size(0), num_patches, x.device)
        prefix_mask = self._make_prefix_mask(prefix_lengths, num_patches)
        attn_mask = self._make_attention_mask(prefix_mask, getattr(self.encoder, "num_special_tokens", 0))
        features = self.encoder.forward_features(x, attn_mask=attn_mask)  # type: ignore[call-arg]
        features = self.encoder.flatten_features(features, include_special_tokens=False)
        pred = self.decoder_embed(features)
        pred = pred + self.decoder_pos_embed
        pred = self.decoder_blocks(pred)
        pred = self.decoder_norm(pred)
        pred = self.decoder_pred(pred)
        loss = self.forward_loss(x, pred, prefix_mask)

        return {"loss": loss, "pred": pred, "mask": prefix_mask, "prefix_lengths": prefix_lengths}


